#!/usr/bin/env npx tsx
/**
 * Generate Identity.API theme CSS from color-themes.ts
 *
 * Imports the derivation engine and all theme configs, then outputs
 * class-scoped CSS rules so Identity.API (Razor Pages) can apply
 * named palettes without a JS runtime.
 *
 * Usage: npx tsx scripts/generate-identity-themes.mjs
 * Re-run when themes change in color-themes.ts.
 */

import { writeFileSync } from "node:fs";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";
import { deriveTheme } from "../src/MediTrack.Web/src/shared/utils/themeDerivation.ts";
import { COLOR_THEME_CONFIGS } from "../src/MediTrack.Web/src/shared/config/color-themes.ts";

const __dirname = dirname(fileURLToPath(import.meta.url));
const OUTPUT_PATH = resolve(__dirname, "../src/Identity.API/wwwroot/css/all-themes.css");

const lines = [
  "/* Auto-generated by scripts/generate-identity-themes.mjs */",
  "/* Source: src/MediTrack.Web/src/shared/config/color-themes.ts */",
  `/* Generated: ${new Date().toISOString().slice(0, 10)} — ${COLOR_THEME_CONFIGS.length} themes */`,
  "",
];

for (const config of COLOR_THEME_CONFIGS) {
  const theme = deriveTheme(config.palette);
  const vars = Object.entries(theme)
    .map(([key, value]) => `  ${key}: ${value};`)
    .join("\n");

  // Use html.theme-{id} selector — Identity's _Layout.cshtml adds this class
  // based on the meditrack-color-theme cookie
  const selector = `html.theme-${config.id}`;
  lines.push(`${selector} {`);
  lines.push(vars);
  lines.push("}");
  lines.push("");
}

writeFileSync(OUTPUT_PATH, lines.join("\n"), "utf-8");
console.log(`Generated ${COLOR_THEME_CONFIGS.length} theme(s) → ${OUTPUT_PATH}`);
